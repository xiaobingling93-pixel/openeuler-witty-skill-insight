from .utils import get_llm


def md_formatter(md_content):
    prompt = f"""
    你是一个专业的Markdown格式化专家。请对以下从PDF提取的文本进行规范化处理：

    任务：
    1. 修复格式问题（多余空行、不规范的标题等）
    2. 优化段落结构，精确描述，不要总结，不要概述
    3. 确保代码块正确格式化
    4. 统一列表和表格格式
    5. 保持内容完整性，不删除重要信息
    6. 对于重要的**版本信息、内核信息、系统信息**需要精确保留（如确需脱敏仅可去除机器名、用户名等个性化标识，不能改动版本号、内核号等关键信息）
    7. 去除无关内容（例如：意见反馈、网站备案信息、版权声明、联系方式、页眉页脚等非正文内容）

    数据脱敏要求：
    1. 对涉及个人隐私、联系方式（手机号、邮箱、微信号等）、身份证号、银行卡号、具体 IP 地址、MAC 地址、详细物理地址等个人信息进行模糊化或泛化处理（如用 *** 或统一占位符替代），避免暴露真实信息
    2. 对域名、主机名、机器名、用户名、部门名等能直接识别组织或个人的标识进行适度泛化（如 company.example.com → example.com 或占位符），在不影响文档理解的前提下弱化真实标识
    3. 对访问令牌、API Key、账号密码、Cookie、密钥材料等强敏感凭证类信息必须完全脱敏（全部替换为占位符），不得保留部分真实内容
    4. 对具体客户名称、机构名称、单位名称、合作方名称及其缩写、项目名等可唯一或高概率指向特定客户/机构的标识信息，应统一进行泛化或替换为通用占位描述（“某场景”），避免暴露真实客户身份
    

    输出要求：
    - 符合标准Markdown语法
    - 保持原文语义不变
    - 使用适当的标题层级
    - 代码块使用正确的语言标识

    注意：只输出修改后的内容，不要包含思考过程或者其他信息

    当前输入的文本内容为：{md_content}
    """
    llm = get_llm()
    message = llm.invoke([{
        "role": "user",
        "content": prompt
    }])
    return message.content